package aka.jmetaagentsgenerator;

public class Root {

    public String name;
    public String serialUID;

    /**
     * @return the name
     */
    public String getName() {
        return this.name;
    }

    /**
     * @param name the name to set
     */
    public void setName(final String name) {
        this.name = name;
    }

    /**
     * @return the serialUID
     */
    public String getSerialUID() {
        return this.serialUID;
    }

    /**
     * @param serialUID the serialUID to set
     */
    public void setSerialUID(final String serialUID) {
        this.serialUID = serialUID;
    }

}
